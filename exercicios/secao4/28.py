'''
28. faça leitura de tres valores e apresente como resultado a soma
dos quadrados dos tres valores lidos.
'''

num1 = float(input('Informe o 1° numero: '))
num2 = float(input('Informe o 2° numero: '))
num3 = float(input('Informe o 3° numero: '))

soma = (num1**2) + (num2**2) + (num3**2)

print(soma)
