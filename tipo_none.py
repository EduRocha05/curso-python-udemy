'''
o Tipo None em python representa o tipo sem tipo, ou poderia ser conhecido tambem como tipo vazio.
o tipo None é SEMPRE especificado com a primeira letra maiuscula.
Certo: None
Errado: none

podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo
antes de recebermos um valor final


Obs: o tipo None é sempre considerado como False
'''
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))
